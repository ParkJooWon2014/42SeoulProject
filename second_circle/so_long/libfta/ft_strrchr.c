#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*tmp;

	tmp = (char *)s + ft_strlen(s);
	while (s <= tmp)
	{
		if (*tmp == (char)c)
			return (tmp);
		tmp --;
	}
	return (NULL);
}
